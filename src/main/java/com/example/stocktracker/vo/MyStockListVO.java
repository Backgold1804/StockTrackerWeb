package com.example.stocktracker.vo;

public class MyStockListVO {
	String my_stock_uid;
	String country;
	String stock_name;
	String ticker;
	String close_price;
	String blended_price;
	String holdings;
	String profit;
	String profit_rate;
	
	public String getMy_stock_uid() {
		return my_stock_uid;
	}
	public void setMy_stock_uid(String my_stock_uid) {
		this.my_stock_uid = my_stock_uid;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getStock_name() {
		return stock_name;
	}
	public void setStock_name(String stock_name) {
		this.stock_name = stock_name;
	}
	public String getTicker() {
		return ticker;
	}
	public void setTicker(String ticker) {
		this.ticker = ticker;
	}
	public String getClose_price() {
		return close_price;
	}
	public void setClose_price(String close_price) {
		this.close_price = close_price;
	}
	public String getBlended_price() {
		return blended_price;
	}
	public void setBlended_price(String blended_price) {
		this.blended_price = blended_price;
	}
	public String getHoldings() {
		return holdings;
	}
	public void setHoldings(String holdings) {
		this.holdings = holdings;
	}
	public String getProfit() {
		return profit;
	}
	public void setProfit(String profit) {
		this.profit = profit;
	}
	public String getProfit_rate() {
		return profit_rate;
	}
	public void setProfit_rate(String profit_rate) {
		this.profit_rate = profit_rate;
	}
	
	
}
